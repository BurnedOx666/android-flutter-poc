class MyHomePage extends StatelessWidget {
  final String title;
  final MethodChannel _channel = const MethodChannel('oneplay');

  const MyHomePage(this.title, {super.key});

  Future<void> _openSecondActivity() async {
    try {
      await _channel.invokeMethod('openSecondActivity');
    } on PlatformException catch (e) {
      print("EXECPTION $e");
    }
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        // Here we take the value from the MyHomePage object that was created by
        // the App.build method, and use it to set our appbar title.
        title: Text(title),
      ),
      body: Center(
        // Center is a layout widget. It takes a single child and positions it
        // in the middle of the parent.
        child: Column(
          mainAxisAlignment: MainAxisAlignment.center,
          children: const <Widget>[
            Text(
              'You have pushed the button this many times:',
            ),
          ],
        ),
      ),
      floatingActionButton: FloatingActionButton(
        onPressed: _openSecondActivity,
        tooltip: 'Increment',
        child: const Icon(Icons.add),
      ), // This trailing comma makes auto-formatting nicer for build methods.
    );
  }

}